package com.fyd.partner.dto;

public class RegistrationNotificationDto {

	protected String partnerRegisteredName;
	protected String emailId;
	public String getPartnerRegisteredName() {
		return partnerRegisteredName;
	}
	public void setPartnerRegisteredName(String partnerRegisteredName) {
		this.partnerRegisteredName = partnerRegisteredName;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	
}
